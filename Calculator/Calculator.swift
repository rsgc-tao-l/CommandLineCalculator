
import Foundation

class Calculator {
    
    // MARK: Properties
    
    // MARK: Initializer
    
    // MARK: Functions / Methods
    func add(this : Double, that: Double) -> Double {
        return this + that
    }
    func minus(this: Double, that: Double) -> Double {
        return this - that
    }
    func divide(this: Double, that: Double) -> Double {
        return this / that
    }
    func multiply(this: Double, that: Double) -> Double {
        return this * that
    }
}
